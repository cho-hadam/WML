package com.jmhd.wml;

public class DiaryList {
    private String title;
    private String date ;

    public void setTitle(String title) {
        this.title = title ;
    }
    public void setDesc(String date) {
        this.date = date ;
    }
    public String getTitle() {
        return this.title ;
    }
    public String getDate() {
        return this.date ;
    }

}